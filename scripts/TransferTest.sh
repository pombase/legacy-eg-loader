#!/bin/bash --

MY_DIR=`dirname $0`
source $MY_DIR/../SetEnv

DBTOTRANSFER=$DBMARTNAME
echo "Dumping ${DBTOTRANSFER} ..."
echo "mysqldump -u$DBPOMBEUSER -p$DBPOMBEPASS -h$DBPOMBEHOST -P$DBPOMBEPORT ${DBTOTRANSFER} | gzip > ${DBTOTRANSFER}.sql.gz'"
echo ""
echo "Transfering ${DBTOTRANSFER} to OY ..."
echo "mysql -u$DBPOMBEUSER -p$DBPOMBEPASS -h$DBPOMBEHOSTOY -P$DBPOMBEPORT -e 'CREATE DATABASE ${DBTOTRANSFER};'"
echo "gunzip < ${DBTOTRANSFER}.sql.gz | mysql -u$DBPOMBEUSER -p$DBPOMBEPASS -h$DBPOMBEHOSTOY -P$DBPOMBEPORT ${DBTOTRANSFER}"
echo ""
echo "Transfering ${DBTOTRANSFER} to PG ..."
echo "mysql -u$DBPOMBEUSER -p$DBPOMBEPASS -h$DBPOMBEHOSTPG -P$DBPOMBEPORT -e 'CREATE DATABASE ${DBTOTRANSFER};'"
echo "gunzip < ${DBTOTRANSFER}.sql.gz | mysql -u$DBPOMBEUSER -p$DBPOMBEPASS -h$DBPOMBEHOSTPG -P$DBPOMBEPORT ${DBTOTRANSFER}"
